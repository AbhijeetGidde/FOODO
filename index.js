function showpop(popId) {
    document.getElementById(popId).style.display = 'flex';
  }

  function closepop() {
    document.getElementById(popId).style.display = 'none';
  }


function onhover(home)
  {home.style.backgroundColor='rgba(255, 255, 0, 0.639)'
home.style.borderRadius='10px'
}

